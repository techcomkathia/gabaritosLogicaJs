//Leia base e altura de um triângulo e calcule sua área	(Área do triângulo: b * h / 2)

let b = Number(prompt('Digite a base ( apenas numeros)')) //leitura e conversão
let h = Number(prompt('Digite a altura ( apenas numeros)')) //leitura e conversão

let area = (b * h) / 2

alert('a area do triângulo é ' + area) // para o usuário
console.log('a area do triângulo é ' + area) // para os desenvolvedores



